public class RelationalOperator {
    public static void main(String[] args) {
        int x = 10 ;
        int y = 8 ;
        
        boolean f = x < y ;
        System.out.println(f);

         f = x > y ;
        System.out.println(f);

         f = x >= y ;
        System.out.println(f);

         f = x != y ;
        System.out.println(f);

         f = x == y ;
        System.out.println(f);

        double a = 8.9;
        double b = 8.7;

        f = a > b ;
        System.out.println(f);


    }
}
